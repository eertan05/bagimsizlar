<p>

Şehrin kültür sanat kapasitesine ve izleyici geliştirme çabasına katkı sunmayı amaçlayan 6x6x6izmir inisiyatifi, 2018 sezonunda katılım odaklı kültür deneyimini çeşitlendirmeye öncelik veren, Karşıyaka, Urla, Seferihisar, Karataş, Kemeraltı ve Alsancak’ı kapsayan bir rota serisi üretti. 30 Haziran - 22 Aralık 2018 tarihleri arasında ayda bir kez hafta sonlarında düzenlenen, toplamda 36 etkinliği içeren rota serisi, izleyiciye gün boyunca güncel sanat, plastik sanatlar, görsel sanatlar, müzik, kentsel hafıza, bağımsız yayıncılık, sürdürülebilir yaşam, mikro tarih ve göç başlıkları altında çeşitli mekânlara yayılan bir deneyim sundu. Bu deneyim, rotada yer alan mekân ve alanların özelliklerine göre tasarlanmış sergileri, performansları, sanatçı konuşmalarını, sunumları, gösterimleri ve söyleşileri içeriyordu. Yapılan yolculuklara her rotaya özel hazırlanmış şarkı listeleri eşlik etti. Proje, sezon boyunca kentte faaliyet gösteren 5 kolektife, 6 inisiyatife, 6 derneğe ve 1 kooperatife temas etti. Düzenlediği etkinliklerde 9 kamusal alanı, 7 sergi salonunu, 2 kitabevini, 3 sanatçı atölyesini, 4 kafeyi, 1 kahvehaneyi, 1 meyhaneyi, 1 kültür merkezini,  3 özel meskeni, 2 dükkânı, 1 ofisi ve 1 barı etkinlik alanı olarak işaretledi ve kullandı. İnisiyatifin Adile Naşit Parkı’nda kendiliğinden örgütlediği forum, kentteki inisiyatif ve kolektiflerin mekânlarını sürdürürken yaşadığı sorunlara odaklanırken, bu sorunların dayanışmayla nasıl aşılabileceğine dair çözüm yolları aradı. Forum, günün sonunda KARANTİNA kolektifinin kurulmasına yol açtı.

