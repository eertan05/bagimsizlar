<p>“Non-Registered Art Communities” projesi, European Cultural Foundation - ECF (Amsterdam) ve MitOst (Berlin) tarafından, Anadolu Kültür’ün (Istanbul) iş birliğinde, TANDEM TURKEY tarafından desteklenmiştir. </p>

